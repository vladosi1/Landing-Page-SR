function initReviewsCarousel() {
  document.querySelectorAll('[data-rv-widget]').forEach(widget => {
    const track = widget.querySelector('[data-rv-track]');
    const prev  = widget.querySelector('[data-rv-prev]');
    const next  = widget.querySelector('[data-rv-next]');
    if (!track || !prev || !next) return;

    let index = 0;
    const getStep = () => {
      const cards = track.children;
      if (cards.length < 2) return track.clientWidth;
      const a = cards[0].getBoundingClientRect();
      const b = cards[1].getBoundingClientRect();
      return Math.abs(b.left - a.left);
    };
    const visible = () => {
      const card = track.children[0];
      if (!card) return 1;
      return Math.max(1, Math.round(track.parentElement.clientWidth / card.getBoundingClientRect().width));
    };
    const maxIndex = () => Math.max(0, track.children.length - visible());

    const apply = () => {
      index = Math.max(0, Math.min(index, maxIndex()));
      track.style.transform = `translateX(${-index * getStep()}px)`;
      prev.disabled = index <= 0;
      next.disabled = index >= maxIndex();
    };

    prev.addEventListener('click', () => { index--; apply(); });
    next.addEventListener('click', () => { index++; apply(); });
    track.querySelectorAll('.rv-readmore').forEach(button => {
      button.addEventListener('click', () => {
        const card = button.closest('.rv-card');
        if (!card) return;

        const expanded = !card.classList.contains('expanded');
        card.classList.toggle('expanded', expanded);
        button.setAttribute('aria-expanded', expanded);
        button.textContent = expanded ? 'Згорнути' : 'Читати далі';
        apply();
      });
    });

    let resizeTimer;
    window.addEventListener('resize', () => {
      clearTimeout(resizeTimer);
      resizeTimer = setTimeout(apply, 80);
    });

    let startX = 0, isDown = false;
    track.addEventListener('pointerdown', e => { isDown = true; startX = e.clientX; track.style.transition = 'none'; });
    track.addEventListener('pointerup', e => {
      if (!isDown) return;
      isDown = false;
      track.style.transition = '';
      const dx = e.clientX - startX;
      if (Math.abs(dx) > 40) { index += dx < 0 ? 1 : -1; }
      apply();
    });
    track.addEventListener('pointercancel', () => { isDown = false; track.style.transition = ''; apply(); });

    apply();
  });
}

function initReveal() {
  const els = document.querySelectorAll('.reveal');
  const io = new IntersectionObserver(entries => {
    entries.forEach(e => { if (e.isIntersecting) { e.target.classList.add('in'); io.unobserve(e.target); } });
  }, { threshold: 0.06, rootMargin: '0px 0px -5% 0px' });
  els.forEach(el => io.observe(el));
  setTimeout(() => els.forEach(el => el.classList.add('in')), 2500);
}
document.readyState === 'loading'
  ? document.addEventListener('DOMContentLoaded', () => { initReveal(); initReviewsCarousel(); })
  : (initReveal(), initReviewsCarousel());
