const progList = document.getElementById('progList');
const progTabs = document.getElementById('progTabs');

function renderProgram(cat) {
  if (!progList) return;

  progList.innerHTML = '';
  const items = cat === 'all' ? PROG : PROG.filter(p => p.cat === cat);
  items.forEach((b, i) => {
    const m = document.createElement('div');
    m.className = 'module' + (i === 0 ? ' open' : '');
    const sectionsHTML = b.sections.map(s => `
      <div class="mb-col">
        <h5>${s.h}</h5>
        <ul>${s.items.map(it => `<li>${it}</li>`).join('')}</ul>
      </div>`).join('');
    m.innerHTML = `
      <button class="module-head" type="button" aria-expanded="${i === 0}">
        <span class="module-n">${String(b.n).padStart(2,'0')}</span>
        <span class="module-title">${b.t}</span>
        <span class="module-cat">${CAT_LABEL[b.cat]}</span>
        <span class="module-toggle"></span>
      </button>
      <div class="module-body">
        <div class="module-body-inner">
          <div class="mb-summary"><strong>Про блок —</strong> ${b.summary}</div>
          ${sectionsHTML}
        </div>
      </div>`;
    m.querySelector('.module-head').addEventListener('click', () => {
      const isOpen = m.classList.contains('open');
      m.classList.toggle('open', !isOpen);
      m.querySelector('.module-head').setAttribute('aria-expanded', !isOpen);
    });
    progList.appendChild(m);
  });
}
