(function initLeadModal() {
  const modal   = document.getElementById('leadModal');
  const form    = document.getElementById('leadForm');
  const success = document.getElementById('leadSuccess');
  if (!modal || !form) return;

  let lastFocused = null;
  const focusableSelector = [
    'a[href]',
    'button:not([disabled])',
    'input:not([disabled])',
    'textarea:not([disabled])',
    'select:not([disabled])',
    '[tabindex]:not([tabindex="-1"])'
  ].join(',');

  function open() {
    lastFocused = document.activeElement;
    modal.hidden = false;
    modal.setAttribute('aria-hidden', 'false');
    document.body.classList.add('lead-modal-open');
    setTimeout(() => {
      const first = form.querySelector('input, textarea');
      first?.focus();
    }, 100);
  }
  function close() {
    modal.hidden = true;
    modal.setAttribute('aria-hidden', 'true');
    document.body.classList.remove('lead-modal-open');
    form.classList.remove('submitted');
    if (success) success.classList.remove('shown');
    form.reset();
    const agree = form.querySelector('input[name="agree"]');
    if (agree) agree.checked = true;
    lastFocused?.focus?.();
  }

  document.addEventListener('click', (e) => {
    const trigger = e.target.closest('a.lead-open, button.lead-open, [data-open="lead"]');
    if (trigger) {
      e.preventDefault();
      open();
    }
    if (e.target.closest('[data-lead-close]')) {
      e.preventDefault();
      close();
    }
  });

  document.addEventListener('keydown', (e) => {
    if (modal.hidden) return;

    if (e.key === 'Escape') {
      close();
      return;
    }

    if (e.key !== 'Tab') return;

    const focusable = Array.from(modal.querySelectorAll(focusableSelector))
      .filter(el => el.offsetParent !== null);
    if (!focusable.length) return;

    const first = focusable[0];
    const last = focusable[focusable.length - 1];
    if (e.shiftKey && document.activeElement === first) {
      e.preventDefault();
      last.focus();
    } else if (!e.shiftKey && document.activeElement === last) {
      e.preventDefault();
      first.focus();
    }
  });

  form.addEventListener('submit', (e) => {
    e.preventDefault();
    if (!form.checkValidity()) { form.reportValidity(); return; }
    const data = Object.fromEntries(new FormData(form).entries());
    window.dispatchEvent(new CustomEvent('landingLeadSubmit', { detail: data }));
    form.classList.add('submitted');
    if (success) success.classList.add('shown');
  });
})();

