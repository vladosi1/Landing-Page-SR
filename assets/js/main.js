function initFormatSpotlight() {
  const blocks = document.querySelectorAll('.fmt-block');
  blocks.forEach(block => {
    block.addEventListener('mousemove', (e) => {
      if (window.innerWidth <= 860) return;
      const rect = block.getBoundingClientRect();
      const x = e.clientX - rect.left;
      const y = e.clientY - rect.top;
      
      block.style.setProperty('--mouse-x', `${x}px`);
      block.style.setProperty('--mouse-y', `${y}px`);
      
      const centerX = rect.width / 2;
      const centerY = rect.height / 2;
      
      const rotateX = -((y - centerY) / centerY) * 6;
      const rotateY = ((x - centerX) / centerX) * 6;
      
      block.style.transform = `perspective(1200px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) scale3d(1.02, 1.02, 1.02)`;
    });
    
    block.addEventListener('mouseleave', () => {
      block.style.transform = '';
      block.style.setProperty('--mouse-x', `-999px`);
      block.style.setProperty('--mouse-y', `-999px`);
    });
  });
}
