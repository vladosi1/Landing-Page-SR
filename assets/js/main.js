// Counter — рахує число від 0 до target з ease-out
function animateCounter(el) {
  const target = parseFloat(el.dataset.counter);
  const duration = parseInt(el.dataset.duration || 1500);
  const suffix = el.dataset.suffix || '';
  const isK = (el.dataset.format === 'k') || suffix.includes('K');
  let startTime = null;
  function step(timestamp) {
    if (!startTime) startTime = timestamp;
    const progress = Math.min((timestamp - startTime) / duration, 1);
    const eased = 1 - Math.pow(1 - progress, 3);
    const value = target * eased;
    let formatted;
    if (suffix.includes('K')) {
      formatted = Math.floor(value);
    } else {
      const intVal = Math.floor(value);
      formatted = intVal.toLocaleString('uk-UA').replace(/\s/g, '\u2009');
    }
    el.textContent = formatted + suffix;
    if (progress < 1) requestAnimationFrame(step);
  }
  requestAnimationFrame(step);
}

const counters = document.querySelectorAll('[data-counter]');
const ioCounter = new IntersectionObserver(entries => {
  entries.forEach(e => {
    if (e.isIntersecting) {
      animateCounter(e.target);
      ioCounter.unobserve(e.target);
    }
  });
}, { threshold: 0.4 });
counters.forEach(c => ioCounter.observe(c));

// Process steps — лінія проростає + кроки активуються по черзі
const stepsSection = document.querySelector('.steps');
if (stepsSection) {
  const ioSteps = new IntersectionObserver(entries => {
    entries.forEach(e => {
      if (e.isIntersecting) {
        e.target.classList.add('in-view');
        const stepsEls = e.target.querySelectorAll('.step');
        stepsEls.forEach((step, i) => {
          setTimeout(() => step.classList.add('active'), 500 + i * 280);
        });
        ioSteps.unobserve(e.target);
      }
    });
  }, { threshold: 0.3 });
  ioSteps.observe(stepsSection);
}

