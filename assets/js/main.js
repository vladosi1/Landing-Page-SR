function initJourney() {
  const slidesEl = document.getElementById('jSlides');
  const vpEl     = document.getElementById('jViewport');
  const section  = document.getElementById('about');
  const nodesEl  = document.getElementById('jProgressNodes');
  if (!slidesEl || !vpEl || !section) return;

  const N = JOURNEY.length;

  /* ── Build slides ── */
  JOURNEY.forEach((s, i) => {
    const blocksHTML = s.blocks.length
      ? `<div class="j-blocks">${s.blocks.map(b => `
          <div class="j-block">
            <div class="j-block-n">${b.n}</div>
            <div>
              <div class="j-block-title">${b.title}</div>
              <div class="j-block-sub">${b.sub}</div>
            </div>
          </div>`).join('')}</div>`
      : `<div class="j-zero-note">Це відправна точка. Тут не потрібно нічого знати — потрібно лише прийти і бути відкритим.</div>`;

    const slide = document.createElement('div');
    slide.className = 'j-slide';
    slide.innerHTML = `
      <div class="j-card">
        <div>
          <div class="j-card-step">
            <span class="j-card-step-num">${String(i+1).padStart(2,'0')}</span>
            <span class="j-card-step-lbl">${s.label}</span>
            <span class="j-card-step-of">${i+1} / ${N}</span>
          </div>
          <span class="j-card-ey">${s.eyebrow}</span>
          <h3>${s.title}</h3>
          ${s.body.map(t => `<p>${t}</p>`).join('')}
        </div>
        ${blocksHTML}
      </div>`;
    slidesEl.appendChild(slide);
  });

  /* ── Build progress nodes ── */
  if (nodesEl) {
    nodesEl.innerHTML = JOURNEY.map((s, idx) => `
      <div class="j-progress-node" data-index="${idx}">
        <div class="j-progress-dot"></div>
        <span class="j-progress-label">${String(idx+1).padStart(2,'0')} &middot; ${s.label}</span>
      </div>
    `).join('');

    nodesEl.querySelectorAll('.j-progress-node').forEach(node => {
      node.addEventListener('click', () => {
        const idx = parseInt(node.getAttribute('data-index'), 10);
        scrollToStep(idx);
      });
    });
  }

  /* ── Measure tallest card to set viewport height ── */
  function adjustViewportHeight() {
    if (window.innerWidth > 820) {
      let maxH = 0;
      const slides = slidesEl.querySelectorAll('.j-slide');
      slides.forEach(slide => {
        const origStyle = slide.style.cssText;
        slide.style.cssText = 'position:absolute;visibility:hidden;display:flex;opacity:0;transform:none;';
        const h = slide.offsetHeight;
        slide.style.cssText = origStyle;
        if (h > maxH) maxH = h;
      });
      vpEl.style.height = `${maxH + 30}px`;
    } else {
      vpEl.style.height = 'auto';
      const slides = slidesEl.querySelectorAll('.j-slide');
      slides.forEach(slide => {
        slide.style.transform = '';
        slide.style.opacity = '';
        slide.style.visibility = '';
      });
    }
  }

  /* ── Scroll Handler ── */
  function handleScroll() {
    if (window.innerWidth <= 820) {
      const slides = slidesEl.querySelectorAll('.j-slide');
      slides.forEach(slide => {
        slide.classList.remove('active', 'past', 'future');
        slide.style.transform = '';
        slide.style.opacity = '';
        slide.style.visibility = '';
      });
      slidesEl.style.transform = '';
      return;
    }

    const rect = section.getBoundingClientRect();
    const scrollHeight = section.offsetHeight;
    const viewportHeight = window.innerHeight;

    const totalScrollable = Math.max(1, scrollHeight - viewportHeight);
    const scrolled = -rect.top;

    let progress = scrolled / totalScrollable;
    progress = Math.max(0, Math.min(1, progress));

    let activeIdx = Math.floor(progress * N);
    if (activeIdx >= N) activeIdx = N - 1;
    if (activeIdx < 0) activeIdx = 0;

    const slides = slidesEl.querySelectorAll('.j-slide');
    slides.forEach((slide, idx) => {
      slide.classList.remove('active', 'past', 'future');
      if (idx === activeIdx) {
        slide.classList.add('active');
      } else if (idx < activeIdx) {
        slide.classList.add('past');
      } else {
        slide.classList.add('future');
      }
    });

    const fillEl = document.getElementById('jProgressFill');
    if (fillEl) {
      fillEl.style.height = `${progress * 100}%`;
    }

    const nodes = document.querySelectorAll('.j-progress-node');
    nodes.forEach((node, idx) => {
      node.classList.remove('active', 'completed');
      if (idx === activeIdx) {
        node.classList.add('active');
      } else if (idx < activeIdx) {
        node.classList.add('completed');
      }
    });
  }

  /* ── Scroll to specific step ── */
  function scrollToStep(idx) {
    if (window.innerWidth <= 820) return;
    const scrollHeight = section.offsetHeight;
    const viewportHeight = window.innerHeight;
    const totalScrollable = Math.max(1, scrollHeight - viewportHeight);

    const targetProgress = (idx + 0.5) / N;
    const targetScrolled = targetProgress * totalScrollable;
    const absoluteSectionTop = window.scrollY + section.getBoundingClientRect().top;

    window.scrollTo({
      top: absoluteSectionTop + targetScrolled,
      behavior: 'smooth'
    });
  }

  adjustViewportHeight();
  window.addEventListener('resize', () => {
    adjustViewportHeight();
    handleScroll();
  });

  window.addEventListener('scroll', handleScroll, { passive: true });
  // Call once initially to set starting positions
  handleScroll();
}
initJourney();

